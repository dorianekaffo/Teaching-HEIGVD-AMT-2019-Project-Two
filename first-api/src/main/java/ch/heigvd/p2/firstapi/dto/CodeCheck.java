package ch.heigvd.p2.firstapi.dto;

public class CodeCheck {

    private String email;
    private String code;

    public CodeCheck() {}

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
